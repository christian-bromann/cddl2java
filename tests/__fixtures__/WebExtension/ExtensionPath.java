package org.openqa.selenium.bidirectional.WebExtension;


public class ExtensionPath {

    public ExtensionPath(String path) {
        this.path = String;
        this.type = "path";
    }

    private final path type;
    public getType(path type) {
        this.type = type;
    }

    private final String path;
    public getPath(String path) {
        this.path = path;
    }

}